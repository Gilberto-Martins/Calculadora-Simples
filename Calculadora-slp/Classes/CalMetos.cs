namespace Calculadora_slp.Classes
{
    public class CalMetos
    {
        public double Soma ( double x, double y)
        {
            return x + y;
        }

        public double Mult (double x, double y)
        {
            return x * y;
        }

         public double Divi (double x, double y)
        {
            return x / y;
        }

         public double Sub (double x, double y)
        {
            return x - y;
        }
    }
}